public class TrainingSearch
{
    private int id;
    private String name;

    public TrainingSearch(int id, String name)
    {
        this.id = id;
        this.name = name;
    }

    public int getId()
    {
        return id;
    }

    public String getName()
    {
        return name;
    }
}
